package EJ5_1;

public class ListaMarea {
    private double altura;
    private String data;
    private String hora;
    private long idPorto;
    private Long idTipoMarea;
    private String tipoMarea;

    public double getAltura() {
        return altura;
    }

    public void setAltura(double altura) {
        this.altura = altura;
    }

    public String getData() {
        return data;
    }

    public void setData(String data) {
        this.data = data;
    }

    public String getHora() {
        return hora;
    }

    public void setHora(String hora) {
        this.hora = hora;
    }

    public long getIdPorto() {
        return idPorto;
    }

    public void setIdPorto(long idPorto) {
        this.idPorto = idPorto;
    }

    public Long getIdTipoMarea() {
        return idTipoMarea;
    }

    public void setIdTipoMarea(Long idTipoMarea) {
        this.idTipoMarea = idTipoMarea;
    }

    public String getTipoMarea() {
        return tipoMarea;
    }

    public void setTipoMarea(String tipoMarea) {
        this.tipoMarea = tipoMarea;
    }

}
